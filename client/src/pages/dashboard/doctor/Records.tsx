import React, { useState } from 'react';
import { Search, FileText, Download, Filter, Eye, X, ChevronDown, CheckCircle, Clock, AlertCircle } from 'lucide-react';

const DoctorRecords = () => {
  const [searchTerm, setSearchTerm] = useState('');
  const [filterStatus, setFilterStatus] = useState('All');
  const [viewRecord, setViewRecord] = useState<any>(null);

  const records = [
    { id: 1, patient: 'John Doe', type: 'Blood Test', date: '2026-04-10', status: 'Reviewed', notes: 'Hemoglobin: 14.5 g/dL (Normal). WBC: 7,200/μL (Normal). Platelets: 250,000/μL (Normal). Overall blood work is within healthy range.', doctor: 'Dr. Sarah Wilson' },
    { id: 2, patient: 'Jane Smith', type: 'MRI Scan', date: '2026-04-12', status: 'Pending Review', notes: 'MRI shows mild lumbar disc herniation at L4-L5 level. No nerve compression detected. Physical therapy recommended. Follow-up in 6 weeks.', doctor: 'Dr. Sarah Wilson' },
    { id: 3, patient: 'Michael Johnson', type: 'X-Ray', date: '2026-04-14', status: 'New', notes: 'Chest X-Ray shows clear lung fields. Heart size is normal. No signs of pneumonia or pleural effusion. Spine alignment normal.', doctor: 'Dr. Sarah Wilson' },
    { id: 4, patient: 'Emily Clark', type: 'ECG Report', date: '2026-04-13', status: 'Reviewed', notes: 'Normal sinus rhythm at 72 bpm. PR interval 0.16s. QRS 0.08s. QTc 420ms. No significant ST changes. Cardiac function appears normal.', doctor: 'Dr. Sarah Wilson' },
    { id: 5, patient: 'Robert Garcia', type: 'Lipid Panel', date: '2026-04-11', status: 'Pending Review', notes: 'Total cholesterol: 215 mg/dL (borderline high). LDL: 138 mg/dL. HDL: 45 mg/dL. Triglycerides: 160 mg/dL. Dietary modifications advised.', doctor: 'Dr. Sarah Wilson' },
  ];

  const downloadRecord = (record: any) => {
    const content = `MEDICAL RECORD\n==============\nPatient: ${record.patient}\nDocument Type: ${record.type}\nDate: ${record.date}\nStatus: ${record.status}\nAttending Physician: ${record.doctor}\n\nClinical Notes:\n${record.notes}\n\n---\nGenerated by HealthAI System on ${new Date().toLocaleString()}\n`;
    const blob = new Blob([content], { type: 'text/plain' });
    const url = window.URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${record.patient.replace(/ /g, '_')}_${record.type.replace(/ /g, '_')}_${record.date}.txt`;
    a.click();
  };

  const filtered = records.filter(r => {
    const matchesSearch = r.patient.toLowerCase().includes(searchTerm.toLowerCase()) || r.type.toLowerCase().includes(searchTerm.toLowerCase());
    const matchesFilter = filterStatus === 'All' || r.status === filterStatus;
    return matchesSearch && matchesFilter;
  });

  const statusConfig: Record<string, { bg: string; color: string; icon: JSX.Element }> = {
    'Reviewed': { bg: '#dcfce7', color: '#166534', icon: <CheckCircle size={13} /> },
    'Pending Review': { bg: '#fef3c7', color: '#92400e', icon: <Clock size={13} /> },
    'New': { bg: '#fee2e2', color: '#991b1b', icon: <AlertCircle size={13} /> },
  };

  return (
    <div className="animate-fade-in">
      {/* View Record Modal */}
      {viewRecord && (
        <div style={{ position: 'fixed', inset: 0, backgroundColor: 'rgba(0,0,0,0.5)', zIndex: 200, display: 'flex', alignItems: 'center', justifyContent: 'center', padding: '20px' }}>
          <div className="glass-card animate-fade-in" style={{ maxWidth: '600px', width: '100%', borderRadius: '24px', padding: '40px', backgroundColor: 'white', position: 'relative' }}>
            <button onClick={() => setViewRecord(null)} style={{ position: 'absolute', top: '20px', right: '20px', backgroundColor: '#f1f5f9', border: 'none', borderRadius: '8px', padding: '8px', cursor: 'pointer' }}>
              <X size={18} />
            </button>
            <div style={{ display: 'flex', gap: '15px', alignItems: 'center', marginBottom: '25px' }}>
              <div style={{ width: '56px', height: '56px', borderRadius: '16px', backgroundColor: '#eff6ff', display: 'flex', alignItems: 'center', justifyContent: 'center' }}>
                <FileText size={28} color="var(--primary)" />
              </div>
              <div>
                <h2 style={{ fontWeight: '800', fontSize: '1.4rem' }}>{viewRecord.type}</h2>
                <p style={{ color: 'var(--text-muted)', fontSize: '0.9rem' }}>{viewRecord.patient} · {viewRecord.date}</p>
              </div>
            </div>
            <div style={{ padding: '20px', backgroundColor: '#f8fafc', borderRadius: '16px', marginBottom: '25px' }}>
              <h4 style={{ fontWeight: '700', marginBottom: '12px', color: 'var(--text-muted)', textTransform: 'uppercase', fontSize: '0.8rem' }}>Clinical Notes</h4>
              <p style={{ lineHeight: '1.8', color: 'var(--text-main)' }}>{viewRecord.notes}</p>
            </div>
            <div style={{ display: 'flex', gap: '12px' }}>
              <button onClick={() => downloadRecord(viewRecord)} className="btn-hover" style={{ flex: 1, padding: '12px', borderRadius: '12px', backgroundColor: 'var(--primary)', color: 'white', fontWeight: '600', display: 'flex', alignItems: 'center', justifyContent: 'center', gap: '8px', cursor: 'pointer' }}>
                <Download size={18} /> Download Record
              </button>
              <button onClick={() => setViewRecord(null)} style={{ flex: 1, padding: '12px', borderRadius: '12px', border: '1px solid var(--border)', backgroundColor: 'white', fontWeight: '600', cursor: 'pointer' }}>
                Close
              </button>
            </div>
          </div>
        </div>
      )}

      <header style={{ marginBottom: '40px' }}>
        <h1 style={{ fontSize: '2.5rem', fontWeight: '800', marginBottom: '10px' }}>Medical Records</h1>
        <p style={{ color: 'var(--text-muted)' }}>Search, view, and download patient medical history and lab reports.</p>
      </header>

      <div style={{ display: 'flex', justifyContent: 'space-between', marginBottom: '30px', flexWrap: 'wrap', gap: '20px' }}>
        <div style={{ position: 'relative', flex: '1', minWidth: '250px', maxWidth: '450px' }}>
          <Search style={{ position: 'absolute', left: '15px', top: '12px', color: 'var(--text-muted)' }} size={20} />
          <input
            type="text"
            placeholder="Search by patient name or record type..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            style={{ padding: '12px 15px 12px 45px', borderRadius: '12px', border: '1px solid var(--border)', outline: 'none', width: '100%' }}
          />
        </div>
        <div style={{ display: 'flex', gap: '12px', flexWrap: 'wrap' }}>
          {['All', 'New', 'Pending Review', 'Reviewed'].map(status => (
            <button key={status} onClick={() => setFilterStatus(status)} style={{ padding: '10px 18px', borderRadius: '10px', border: `1px solid ${filterStatus === status ? 'var(--primary)' : 'var(--border)'}`, backgroundColor: filterStatus === status ? 'var(--primary)' : 'white', color: filterStatus === status ? 'white' : 'var(--text-main)', fontWeight: '600', fontSize: '0.9rem', cursor: 'pointer', transition: '0.2s' }}>
              {status}
            </button>
          ))}
        </div>
      </div>

      <div className="glass-card" style={{ borderRadius: '24px', overflowX: 'auto' }}>
        <table style={{ width: '100%', borderCollapse: 'collapse', textAlign: 'left', minWidth: '700px' }}>
          <thead style={{ backgroundColor: '#f8fafc', borderBottom: '1px solid var(--border)' }}>
            <tr>
              <th style={{ padding: '20px', color: 'var(--text-muted)', textTransform: 'uppercase', fontSize: '0.85rem' }}>Document</th>
              <th style={{ padding: '20px', color: 'var(--text-muted)', textTransform: 'uppercase', fontSize: '0.85rem' }}>Patient</th>
              <th style={{ padding: '20px', color: 'var(--text-muted)', textTransform: 'uppercase', fontSize: '0.85rem' }}>Date</th>
              <th style={{ padding: '20px', color: 'var(--text-muted)', textTransform: 'uppercase', fontSize: '0.85rem' }}>Status</th>
              <th style={{ padding: '20px', color: 'var(--text-muted)', textTransform: 'uppercase', fontSize: '0.85rem', textAlign: 'right' }}>Actions</th>
            </tr>
          </thead>
          <tbody>
            {filtered.map((r) => (
              <tr key={r.id} style={{ borderBottom: '1px solid var(--border)' }}>
                <td style={{ padding: '20px' }}>
                  <div style={{ display: 'flex', alignItems: 'center', gap: '12px' }}>
                    <div style={{ width: '40px', height: '40px', borderRadius: '10px', backgroundColor: '#f1f5f9', display: 'flex', alignItems: 'center', justifyContent: 'center' }}>
                      <FileText size={20} color="var(--primary)" />
                    </div>
                    <span style={{ fontWeight: '700' }}>{r.type}</span>
                  </div>
                </td>
                <td style={{ padding: '20px', fontWeight: '600' }}>{r.patient}</td>
                <td style={{ padding: '20px', color: 'var(--text-muted)' }}>{r.date}</td>
                <td style={{ padding: '20px' }}>
                  <span style={{ padding: '5px 10px', borderRadius: '6px', fontSize: '0.8rem', fontWeight: '700', backgroundColor: statusConfig[r.status].bg, color: statusConfig[r.status].color, display: 'inline-flex', alignItems: 'center', gap: '5px' }}>
                    {statusConfig[r.status].icon} {r.status}
                  </span>
                </td>
                <td style={{ padding: '20px', textAlign: 'right' }}>
                  <div style={{ display: 'flex', gap: '10px', justifyContent: 'flex-end' }}>
                    <button onClick={() => setViewRecord(r)} className="btn-hover" style={{ padding: '8px 14px', borderRadius: '8px', border: '1px solid var(--border)', backgroundColor: 'white', display: 'flex', alignItems: 'center', gap: '6px', fontSize: '0.85rem', fontWeight: '600', cursor: 'pointer' }}>
                      <Eye size={15} /> View
                    </button>
                    <button onClick={() => downloadRecord(r)} className="btn-hover" style={{ padding: '8px 14px', borderRadius: '8px', border: '1px solid var(--primary)', backgroundColor: 'var(--primary)', color: 'white', display: 'flex', alignItems: 'center', gap: '6px', fontSize: '0.85rem', fontWeight: '600', cursor: 'pointer' }}>
                      <Download size={15} /> Download
                    </button>
                  </div>
                </td>
              </tr>
            ))}
            {filtered.length === 0 && (
              <tr><td colSpan={5} style={{ padding: '60px', textAlign: 'center', color: 'var(--text-muted)' }}>No records match your filters.</td></tr>
            )}
          </tbody>
        </table>
      </div>
    </div>
  );
};

export default DoctorRecords;
